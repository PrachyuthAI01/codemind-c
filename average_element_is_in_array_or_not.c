#include<stdio.h>
int main()
{
    int i,n,a[100],sum=0,t,s=0;
    scanf("%d",&n);
    for(i=0;i<n;i++)
    {
        scanf("%d",&a[i]);
    }
    for(i=0;i<n;i++)
    {
        sum=sum+a[i];
    }
    t=sum/n;
    for(i=0;i<n;i++)
    {
        if(t==a[i])
        {
            s=1;
        }
    }
    if(s==1)
    {
        printf("True");
    }
    else
    {
        printf("False");
    }
}